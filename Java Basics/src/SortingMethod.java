public class SortingMethod {

	public static void main(String[] args) {
		int no[]= {20,40,70,50,10,60};
		String s1[]= {"hello","welcome","java"};
		Arrays.sort(no);//sorting array using sort method of Arrays Class
		for(int i:no)
		{
			System.out.println(i);
		}
		for(int i:no)
		{
			System.out.println(i);
		}
		for(String s:s1)
		{
			System.out.println(s);
		}

	}

}
